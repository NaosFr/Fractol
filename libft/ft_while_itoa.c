#include "libft.h"

char		*ft_while_itoa(char *str, int len_int, int nbr)
{
	while (len_int >= 0)
	{
		str[len_int] = '0' + (nbr % 10);
		len_int--;
		nbr /= 10;
	}
	return (str);
}
